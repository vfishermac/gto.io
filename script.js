// Функция для проверки результата
function checkResult(input, normValue) {
  const userResult = parseInt(input.value);
  const normNumber = parseInt(normValue);

  if (userResult === normNumber) {
    input.style.backgroundColor = "lightgreen"; // Зелёный цвет при совпадении
  } else {
    input.style.backgroundColor = "lightcoral"; // Красный цвет при несовпадении
  }
}
